export function getPortfolioByCategory(category: string) {
  return portfolioData.filter(project => project.category === category || project.categoryAr === category)
}

export function getPortfolioById(id: string) {
  return portfolioData.find(project => project.id === id)
}

export function getAllPortfolioCategories() {
  const categories = new Set()
  portfolioData.forEach(project => {
    categories.add(project.category)
    categories.add(project.categoryAr)
  })
  return Array.from(categories)
}
